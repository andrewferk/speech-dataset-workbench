"""The privacy allowlist (ADR-0002/ADR-0009).

The rule is "captured audio never enters git; generated audio may." Every tracked ``*.wav``
must therefore live in one of the two trees whose WAVs are generated by ``tests/synth.py`` —
the test fixtures and the example ``--data-in`` — so a real captured recording can never be
committed unnoticed. This is the one failure a revert cannot undo, so it lands here rather than
at the end of the project.

Scope is deliberately WAV-only: a stray ``.mp3`` is an ingest abort (ADR-0005), not a privacy
breach, so it is not this test's concern.
"""

import subprocess
from pathlib import Path

REPO_ROOT = Path(__file__).parents[2]

# The only trees whose WAVs are synthetic (born under the generator, ADR-0008/ADR-0009).
ALLOWED_PREFIXES = ("tests/fixtures/", "examples/data-in/")


def _tracked_wavs() -> list[str]:
    result = subprocess.run(
        ["git", "ls-files", "-z", "*.wav", "*.WAV"],
        cwd=REPO_ROOT,
        capture_output=True,
        text=True,
        check=True,
    )
    return [name for name in result.stdout.split("\0") if name]


def test_every_tracked_wav_is_synthetic() -> None:
    offenders = [name for name in _tracked_wavs() if not name.startswith(ALLOWED_PREFIXES)]
    assert not offenders, (
        "tracked WAV(s) outside the synthetic-audio trees "
        f"{ALLOWED_PREFIXES}: {offenders}. Captured audio must never enter git (ADR-0002)."
    )
